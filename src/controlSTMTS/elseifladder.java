package controlSTMTS;

public class elseifladder {

	public static void main(String[] args) 
	{
		int x=10,y=20,z=25;
		if(x>y && x>z) 
			{
			System.out.println("x is greater");
			}
		else if(y>x && y>z)
		{
		System.out.println("y is greater");
		}

	   
		else 
			{
			System.out.println("z is greater");
	   
			}
}
}
